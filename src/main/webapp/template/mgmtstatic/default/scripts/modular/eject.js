//提示弹出框 
jQuery(document).ready(function($) {
	$('#operation').click(function(){
	$('#eject-mask').fadeIn(100);
	$('#prompt').slideDown(200);
	})
	$('#prompt-close').click(function(){
	$('#eject-mask').fadeOut(100);
	$('#prompt').slideUp(150);
	})
	$('.eject-medium-title .img').click(function(){
	$('#eject-mask').fadeOut(100);
	$('#prompt').slideUp(150);
	})
})

